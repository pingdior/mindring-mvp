// 语言管理器类
class LanguageManager {
    constructor() {
        this.currentLanguage = 'en';
        this.supportedLanguages = {
            'en': 'English',
            'zh': '中文',
            'fr': 'Français',
            'es': 'Español',
            'de': 'Deutsch',
            'ar': 'العربية',
            'pt': 'Português',
            'it': 'Italiano',
            'ja': '日本語',
            'ko': '한국어'
        };
        this.init();
    }

    init() {
        this.detectLanguage();
        this.setupLanguageSelector();
        this.updateContent();
    }

    detectLanguage() {
        // 从localStorage获取保存的语言设置
        const savedLanguage = localStorage.getItem('mindring-language');
        if (savedLanguage && this.supportedLanguages[savedLanguage]) {
            this.currentLanguage = savedLanguage;
            return;
        }

        // 根据浏览器语言自动检测
        const browserLanguage = navigator.language || navigator.userLanguage;
        const languageCode = browserLanguage.split('-')[0].toLowerCase();
        
        // 映射浏览器语言到支持的语言
        const languageMap = {
            'zh': 'zh',
            'fr': 'fr',
            'es': 'es',
            'de': 'de',
            'ar': 'ar',
            'pt': 'pt',
            'it': 'it',
            'ja': 'ja',
            'ko': 'ko'
        };

        this.currentLanguage = languageMap[languageCode] || 'en';
    }

    setupLanguageSelector() {
        const languageBtn = document.getElementById('languageBtn');
        const languageDropdown = document.getElementById('languageDropdown');
        const languageText = document.querySelector('.language-text');
        
        if (!languageBtn || !languageDropdown) return;

        // 更新当前语言显示
        this.updateLanguageButton();

        // 设置语言选项点击事件
        const languageOptions = languageDropdown.querySelectorAll('.language-option');
        languageOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                const selectedLang = e.target.getAttribute('data-lang');
                if (selectedLang) {
                    this.changeLanguage(selectedLang);
                    // 关闭下拉菜单
                    languageDropdown.classList.remove('active');
                }
            });
        });

        // 设置按钮点击事件来切换下拉菜单
        languageBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            languageDropdown.classList.toggle('active');
        });

        // 点击页面其他地方关闭下拉菜单
        document.addEventListener('click', () => {
            languageDropdown.classList.remove('active');
        });

        // 阻止下拉菜单内部点击事件冒泡
        languageDropdown.addEventListener('click', (e) => {
            e.stopPropagation();
        });
    }

    updateLanguageButton() {
        const languageText = document.querySelector('.language-text');
        const languageOptions = document.querySelectorAll('.language-option');
        
        if (languageText) {
            // 更新按钮显示的语言
            const currentLangName = this.supportedLanguages[this.currentLanguage];
            languageText.textContent = currentLangName;
        }

        // 更新选中状态
        languageOptions.forEach(option => {
            const lang = option.getAttribute('data-lang');
            if (lang === this.currentLanguage) {
                option.classList.add('selected');
            } else {
                option.classList.remove('selected');
            }
        });
    }

    changeLanguage(languageCode) {
        if (!this.supportedLanguages[languageCode]) return;
        
        this.currentLanguage = languageCode;
        localStorage.setItem('mindring-language', languageCode);
        this.updateContent();
        this.updatePageDirection();
        this.updateLanguageButton();
    }

    updateContent() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            if (translation) {
                // 检查翻译内容是否包含HTML标签
                if (translation.includes('<') && translation.includes('>')) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });

        // 更新页面标题
        const titleKey = document.documentElement.getAttribute('data-i18n');
        if (titleKey) {
            const titleTranslation = this.getTranslation(titleKey);
            if (titleTranslation) {
                document.title = titleTranslation;
            }
        }
    }

    updatePageDirection() {
        // 为阿拉伯语设置RTL方向
        if (this.currentLanguage === 'ar') {
            document.documentElement.setAttribute('dir', 'rtl');
            document.documentElement.setAttribute('lang', 'ar');
        } else {
            document.documentElement.setAttribute('dir', 'ltr');
            document.documentElement.setAttribute('lang', this.currentLanguage);
        }
    }

    getTranslation(key) {
        const languageData = translations[this.currentLanguage];
        if (!languageData) return null;
        
        return languageData[key] || translations.en[key] || null;
    }

    getCurrentLanguage() {
        return this.currentLanguage;
    }
}

// 初始化语言管理器
document.addEventListener('DOMContentLoaded', () => {
    window.languageManager = new LanguageManager();
});